// PROGRAM FOR ASSIGMENT ORDER

#include<stdio.h>

int main()
{
    int a=10;
    printf("%d\n",a); //ASSIGNING VALUE 10 TO A

    a+=10;
    printf("%d\n",a); //ASSIGNING VALUE BY ADDING 10 TO A

    a*=10;
    printf("%d\n",a); //ASSIGNINGN VALUE BY MULTIPLYING 10 TO A

    a%=10;
    printf("%d\n",a); //ASSIGNING VALUE BY MOD 10 FROM A

    return 0;
}
